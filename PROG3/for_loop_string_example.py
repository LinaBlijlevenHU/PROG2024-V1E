"""
for_loop_string_example.py

Strings gedragen zich net als lijsten in Python: alsof ze uit losse karakters bestaan.
Daarom kunnen we in deze taal over elk karakter van een string heen loopen.
"""
name = 'Apple'

for char in name:
    # Op de regel waar je print, kun je een breakpoint neerzetten.
    # Als je vervolgens op het insect naast de run-knop klikt, kun je
    # stap voor stap zien hoe de for-loop wordt uitgevoerd. Dit kan
    # nuttig zijn tijdens het schrijven van je eigen programma's!
    print(char)
