# Plus
print(1 + 1)

# Minus
print(5 - 3)
print(-1)

# Keer
print(5 * 2)        # 10

# Gedeeld door
print(5 / 2)        # 2.5

# Floor division (gedeeld door en naar beneden afronden) //
print(5 // 2)       # 2 (want 2 * 2 = 4 en dan nog 1 restje)

# Modulo %
print(5 % 2)

# Tot de macht
print(2**3)

# Wortel
print(9**(1/2))

# Absolute waarde
print(abs(-2))

# Bepaal minimale waarde
print(min(5, 3, 1, 0, 4))
# Bepaal grootste waarde
print(max(5, 3, 1, 0, 4))